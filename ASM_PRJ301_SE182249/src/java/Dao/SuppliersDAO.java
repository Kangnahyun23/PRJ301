package Dao;

import Model.SuppliersDTO;
import Utils.DBUtils;
import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author PC
 */
public class SuppliersDAO {

    private static final String SELECTALL = "SELECT * FROM Suppliers";
    private static final String DELETE = "DELETE Suppliers WHERE categoryId=?";
    private static final String UPDATE = "UPDATE Suppliers SET companyName=?,address=?,phone=? WHERE supplierId=?";
    private static final String INSERT = "INSERT INTO Suppliers values (?,?,?)";

    public List<SuppliersDTO> getListSuppliers() throws SQLException {
        List<SuppliersDTO> list = new ArrayList<>();
        Connection conn = null;
        PreparedStatement ptm = null;
        ResultSet rs = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(SELECTALL);
                rs = ptm.executeQuery();
                while (rs.next()) {
                    int supplierId = rs.getInt("supplierId");
                    String companyName = rs.getString("companyName");
                    String address = rs.getString("address");
                    String phone = rs.getString("phone");
                    list.add(new SuppliersDTO(supplierId, companyName, address, phone));
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (rs != null) {
                rs.close();
            }
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return list;
    }

    public boolean delete(int supplierId) throws SQLException {
        boolean checkDelete = false;
        Connection conn = null;
        PreparedStatement ptm = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(DELETE);
                ptm.setInt(1, supplierId);
                checkDelete = ptm.executeUpdate() > 0 ? true : false;
            }
        } catch (Exception e) {
        } finally {
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return checkDelete;
    }

    public boolean update(SuppliersDTO supplier) throws SQLException {
        boolean checkUpdate = false;
        Connection conn = null;
        PreparedStatement ptm = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(UPDATE);
                ptm.setString(1, supplier.getCompanyName());
                ptm.setString(2, supplier.getAddress());
                ptm.setString(3, supplier.getPhone());
                ptm.setInt(4, supplier.getSupplierId());
                checkUpdate = ptm.executeUpdate() > 0 ? true : false;
            }
        } catch (Exception e) {
        } finally {
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return checkUpdate;
    }

    public boolean insert(SuppliersDTO supplier) throws ClassNotFoundException, SQLException {
        boolean checkInsert = false;
        Connection conn = null;
        PreparedStatement ptm = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(INSERT);
                ptm.setString(1, supplier.getCompanyName());
                ptm.setString(2, supplier.getAddress());
                ptm.setString(3, supplier.getPhone());
                checkInsert = ptm.executeUpdate() > 0 ? true : false;
            }
        } finally {
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return checkInsert;
    }
}
